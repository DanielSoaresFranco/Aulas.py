n1 = int(input('um valor: '))
n2 = int(input('outro valor: '))
so = n1 + n2
su = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2
rd = n1 % n2
print('a soma vale {}, \na subtração vale {}, \no produto vale {}, \na divisão vale {:.2f}, \na potência vale {}, \na divisão inteira vale {}, \no resto da divisão vale {}'.format(so, su, m, d, e, di, rd))
