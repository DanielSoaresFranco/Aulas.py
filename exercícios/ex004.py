thing = input('digite algo: ')
print('o tipo primitivo desse valor é \033[1;30m{}\033[m.'.format(type(thing)))
print('Alfanumérico: \033[1;31m{}\033[m.'.format(thing.isalnum()))
print('Numérico: \033[1;32m{}\033[m.'.format(thing.isnumeric()))
print('Alfabético: \033[1;33m{}\033[m.'.format(thing.isalpha()))
print('MAIÚSCULO: \033[1;34m{}\033[m.'.format(thing.isupper()))
print('minúsculo: \033[1;35m{}\033[m.'.format(thing.islower()))
print('Só espaços: \033[1;36m{}\033[m.'.format(thing.isspace()))
print('Capitalizada: \033[1;37m{}\033[m.'.format(thing.istitle()))
